						<p>Sebuah pertandingan diperintah oleh seorang wasit yang mempunyai “wewenang penuh untuk menjalankan pertandingan sesuai Peraturan Permainan dalam suatu pertandingan yang telah diutuskan kepadanya” (Peraturan 5), dan keputusan-keputusan pertandingan yang dikeluarkannya dianggap sudah final. Sang wasit dibantu oleh dua orang asisten wasit (dulu dipanggil hakim/penjaga garis). Dalam banyak pertandingan wasit juga dibantu seorang ofisial keempat yang dapat menggantikan seorang ofisial lainnya jika diperlukan.</p>
						<p>Apabila pemain melakukan pelanggaran yang cukup keras maka wasit dapat memberikan peringatan dengan kartu kuning atau kartu merah.Pertandingan akan dihentikan dan wasit menunjukkan kartu ke depan pemain yang melanggar kemudian mencatat namanya di dalam buku.Kartu kuning merupakan peringatan atas pelanggaran seperti bersikap tidak sportif, secara terus-menerus melanggar peraturan, berselisih kata-kata atau tindakan, menunda memulai kembali pertandingan, keluar-masuk pertandingan tanpa persetujuan wasit, ataupun tidak menjaga jarak dari pemain lawan yang sedang melakukan tendangan bebas atau lemparan ke dalam. Pemain yang menerima dua kartu kuning akan mendapatkan kartu merah dan keluar dari pertandingan.</p>

<p>Pemain yang mendapatkan kartu merah harus keluar dari pertandingan tanpa bisa digantikan dengan pemain lainnya. Beberapa contoh tindakan yang dapat diganjar kartu merah adalah pelanggaran berat yang membahayakan atau menyebabkan cedera parah pada lawan, meludah, melakukan kekerasan, melanggar lawan yang sedang berusaha mencetak gol, menyentuh bola untuk mencegah gol, dan menggunakan bahasa atau gerak tubuh yang cenderung menantang.</p>

<p>Dalam pertandingan profesional, terdapat 4 petugas yang memimpin jalannya pertandingan, yaitu wasit, 2 penjaga garis, dan seorang petugas di pinggir tengah lapangan. Wasit memiliki peluit yang menandakan apakah saat berhenti atau memulai memainkan bola. Dia juga bertugas memberikan hukuman dan peringatan atas pelanggaran yang terjadi di lapangan. Masing-masing penjaga garis bertanggung jawab mengawasi setengah bagian dari lapangan.Mereka membawa bendera dengan warna terang untuk menandakan adanya pelanggaran, bola keluar, ataupun offside. Biasanya mereka akan bergerak mengikuti posisi pemain belakang terakhir.</p>

<p>Petugas terakhir memiliki tugas untuk mencatat semua waktu yang sempat terhenti selama pertandingan berlangsung dan memberikan info mengenai tambahan waktu di akhir setiap babak. Petugas ini juga bertugas memeriksa pergantian pemain dan menjadi penghubung antara manager tim dengan wasit. Dalam beberapa pertandingan, teknologi penggunaan video atau penggunaan orang kelima untuk menentukan ketepatan keputusan wasit mulai digunakan.isalnya yang menentukan apakah suatu bola telah melewati garis atau apakah seorang pemain berada dalam keadaan offside ketika mencetak gol.</p>
